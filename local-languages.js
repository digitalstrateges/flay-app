    getLanguage(code) {
        return this.languages[code] || this.languages.fr;
    }

    getAllLanguages() {
        return Object.values(this.languages);
    }

    getLocalIvorianLanguages() {
        return Object.values(this.languages).filter(lang => 
            lang.region && lang.region.includes('CI') || 
            ['dyu', 'bci', 'bet', 'snn', 'ago', 'any', 'att', 'dyg', 'gwa', 'loh', 'kul', 'nyz', 'god', 'nie'].includes(lang.code)
        );
    }

    getRTLLanguages() {
        return Object.values(this.languages).filter(lang => lang.rtl === true);
    }

    getTranslation(langCode, key) {
        const lang = this.translations[langCode] || this.translations.fr;
        return lang[key] || this.translations.fr[key] || key;
    }

    getAllTranslations(langCode) {
        return this.translations[langCode] || this.translations.fr;
    }

    // Direction helpers
    isRTL(langCode) {
        const lang = this.languages[langCode];
        return lang?.rtl === true;
    }

    getDirection(langCode) {
        const lang = this.languages[langCode];
        return lang?.direction || 'ltr';
    }

    getDirAttribute(langCode) {
        return this.isRTL(langCode) ? 'rtl' : 'ltr';
    }
